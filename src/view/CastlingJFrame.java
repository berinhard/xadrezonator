    /** Creates new form CastlingJFrame */
    public CastlingJFrame(Game gameParam, Player playerParam, Rectangle r) {
        initComponents();
        this.game = gameParam;
        this.player = playerParam;
        if (this.game.availLongCastling(this.player))
            buttonLong.setEnabled(true);
        else
            buttonLong.setEnabled(false);
        if (this.game.availShortCastling(this.player))
            buttonShort.setEnabled(true);
        else
            buttonShort.setEnabled(false);
        
        setBounds((int)((r.getX() + r.getWidth()) - getWidth()) / 2,
                ((int)(r.getY() + r.getHeight()) - getHeight()) / 3,
                getWidth(), getHeight());
    }

